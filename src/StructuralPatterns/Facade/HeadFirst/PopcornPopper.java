package StructuralPatterns.Facade.HeadFirst;

class PopcornPopper {
    public void on() {
        System.out.println("Popcorn Popper on");
    }

    public void off() {
        System.out.println("Popcorn Popper off");
    }

    public void pop() {
        System.out.println("Popcorn Popper doing popcorn!");
    }

}
